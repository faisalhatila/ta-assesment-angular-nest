#!/usr/bin/env node
/**
 * Writes `src/environments/environment.prod.secrets.ts` from process.env.
 * Run before `ng build` on Netlify (or any CI) so the production bundle gets real URLs/keys.
 *
 * Netlify: Site configuration → Environment variables:
 *   SUPABASE_URL          e.g. https://xxxxx.supabase.co
 *   SUPABASE_ANON_KEY     Project API anon key (public; safe in browser bundle)
 *   API_BASE_URL          optional, e.g. https://your-api.example.com/api
 *
 * Variables must be available at **build** time (scope "All" or "Builds", not "Functions" only).
 */
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outFile = join(__dirname, '../src/environments/environment.prod.secrets.ts');

const apiBaseUrl = process.env.API_BASE_URL ?? '';
const supabaseUrl = process.env.SUPABASE_URL ?? '';
const supabaseAnonKey = process.env.SUPABASE_ANON_KEY ?? '';

const hasUrl = Boolean(supabaseUrl.trim());
const hasKey = Boolean(supabaseAnonKey.trim());

if (process.env.NETLIFY === 'true' && (!hasUrl || !hasKey)) {
  console.error(
    '\n[write-prod-secrets] SUPABASE_URL / SUPABASE_ANON_KEY missing or empty at build time.',
  );
  console.error(
    '  → Netlify: Site configuration → Environment variables → add both (same as backend .env).',
  );
  console.error(
    '  → Each variable: Edit → "Scopes" must include **Builds** (or **All**), not runtime-only.\n',
  );
  console.error(
    '  → Build continues; login will not work until the next deploy after you set them.\n',
  );
}

const content = `/* Generated by scripts/write-prod-secrets.mjs — do not edit by hand */
export const prodSecrets = {
  apiBaseUrl: ${JSON.stringify(apiBaseUrl)},
  supabaseUrl: ${JSON.stringify(supabaseUrl)},
  supabaseAnonKey: ${JSON.stringify(supabaseAnonKey)},
} as const;
`;

writeFileSync(outFile, content, 'utf8');
console.log('write-prod-secrets: wrote', outFile);
